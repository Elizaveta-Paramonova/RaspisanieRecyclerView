package com.example.raspisanierecyclerview;

public class Item {
    String day, para1, para2, para3, para4, para5;

    public Item(String day, String para1, String para2, String para3, String para4, String para5) {
        this.day = day;
        this.para1 = para1;
        this.para2 = para2;
        this.para3 = para3;
        this.para4 = para4;
        this.para5 = para5;
    }

    public String getDay() {
        return day;
    }

    public String getPara1() {
        return para1;
    }

    public String getPara2() {
        return para2;
    }

    public String getPara3() {
        return para3;
    }

    public String getPara4() {
        return para4;
    }

    public String getPara5() {
        return para5;
    }
}
